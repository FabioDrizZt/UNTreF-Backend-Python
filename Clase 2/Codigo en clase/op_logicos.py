# &&
print(True and True)
print(True and False)
print(False and True)
print(False and False)
# ||
print(True or True)
print(True or False)
print(False or True)
print(False or False)
# !
print(not True)
print(not False)

""" Existen otros operadores pero binarios """

print(1001 << 1) # Shift Left
print(1001 >> 1) # Shift Right
print(1001 & 1101) # AND
print(1001 | 1) # OR
print(1001 ^ 1) # XOR
print(~1001) # NOT

