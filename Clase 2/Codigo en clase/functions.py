def sumar(a, b):
    return a + b

def multiplicar(a, b):
    return a * b

def dividir(a, b):
    return a / b

def menor(a, b):
    if a < b:
        return a
    else:
        return b

def mayor(a, b):
    if a > b:
        return a
    else:
        return b
      
print(sumar(1, 2))
print(multiplicar(2, 3))
print(dividir(4, 2))
print(menor(1, 2))
print(mayor(1, 2))